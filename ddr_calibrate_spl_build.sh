#!/bin/bash

export DTS_BOARD=stm32mp153a-stp-stm32mp153aabx-mx

if [ -z ${CROSS_COMPILE} ]; then
    export CROSS_COMPILE=arm-linux-gnueabihf-
fi
export ARCH=arm

if [ -f arch/arm/dts/${DTS_BOARD}.dts ]; then

    if [ -z $* ]; then
	[ -f .config ] || make stm32mp15_basic_defconfig
    fi

    echo Build DDR calibration tools
    DEVICE_TREE=${DTS_BOARD} DDR_INTRACTIVE=1 make $*
    [ -f u-boot-spl.stm32 ] && echo "Compiled done"
else
    echo "  You need place file ${DTS_BOARD}.dts"
    echo "(generated by CubeMX and fixed for stdout path) by writing"
    echo "	aliases {"
    echo "		serial0 = &uartX;"
    echo "		serial1 = &usartY;"
    echo "	};"
    echo
    echo "	chosen {"
    echo "		stdout-path = \"serialZ:115200n8\";"
    echo "	};"
    echo "into arch/arm/dts directory and run this again."
fi